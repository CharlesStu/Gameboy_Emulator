//Memory Struct
typedef struct MMU
{
	uint8_t bootRom[0x100];
	struct
	{
		union
		{
			uint8_t memory_space[0x10000];

			struct
			{
				uint8_t rom[2][0x4000];
				uint8_t vram[0x2000];
				uint8_t eram[0x2000];
				uint8_t wram[0x2000];
				uint8_t prohibited[0x1E00];
				uint8_t oam[0xA0];
				uint8_t prohibited1[0x60];
				uint8_t io[0x40];
				uint8_t ppu[0x40];
				uint8_t zram[0x80];
				uint8_t ie;
			};
		};
	};
} MMU;
//Create MMU, return MMU
MMU* MMU_create()
{
	MMU* memory = (MMU*)calloc(1,sizeof(MMU));
	return memory;
}
//Destory MMU void 
void MMU_destroy(MMU* memory)
{
	free(memory);
	memory = NULL;
}
//Memory Read 8 and 16 bit
uint8_t MMU_read_8(MMU* memory, int address)
{
	printf("reading 8bits from %u\n",(memory->memory_space+address));

	return *((uint8_t*)memory->memory_space + address);
}

uint16_t MMU_read_16(MMU* memory, int address)
{
	printf("reading 16 bits from %u\n",(memory->memory_space+address));

	return *((uint16_t*)(memory->memory_space + address));
}

//Memory Write 8 and 16 bit 
void MMU_write_8(MMU* memory, int address, uint8_t data)
{
	printf("writing 8 bits to %u\n",((uint8_t*)memory->memory_space+address));
	*((uint8_t*)memory->memory_space+address) = data;
}

void MMU_write_16(MMU* memory, int address, uint16_t data)
{
	printf("writing 16 bits to %u\n",(memory->memory_space+address));
	*((uint16_t*)(memory->memory_space+address)) = data;
}

//Load bootrom into memory
void MMU_load_bootrom(MMU* memory)
{
	memcpy((void*)memory->bootRom, (const void*)bootRom, sizeof(bootRom));
}